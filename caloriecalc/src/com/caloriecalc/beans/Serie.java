package com.caloriecalc.beans;

import java.util.Date;


public class Serie {
	
	//x coordinate attribute
	private Date x;
	//y coordinate attribute
	private Double y;
	
	
	/**
	 * @return the x
	 */
	public Date getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(Date x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public Double getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(Double y) {
		this.y = y;
	}

}
